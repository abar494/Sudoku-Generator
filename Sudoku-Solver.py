def findEmpty(board):

    # Iterate through board array
    for i in range(len(board)):
        for j in range(len(board)):
            # At each square, check if value is 0
            if board[i][j] == 0:
                # return i (row co-ord) and j (column co-ord)
                return i, j

    return None
